# Запитуємо вхідні дані у користувача
number = int(input("Введіть 5-ти значне число: "))

# Витягуємо цифри за допомогою // та %
n1 = number // 10000               # перша цифра
n2 = (number // 1000) % 10         # друга цифра
n3 = (number // 100) % 10          # третя цифра
n4 = (number // 10) %10            # четверта цифра
n5 = number % 10                   # пята цифра

# Виводимо результат
print(type(number))
print(n5, n4, n3, n2, n1)



